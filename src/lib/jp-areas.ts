/**
 * Japanese ward/district-level coordinate database.
 * Covers all wards of designated cities (政令指定都市) + Tokyo 23 special wards.
 */

export interface AreaEntry {
  name: string
  lat: number
  lon: number
  label: string // parent city/prefecture
}

export interface CityWithWards {
  city: string
  label: string
  lat: number
  lon: number
  wards: AreaEntry[]
}

export interface RegionGroup {
  region: string
  cities: CityWithWards[]
}

// Helper to build ward entries
function w(name: string, lat: number, lon: number, label: string): AreaEntry {
  return { name, lat, lon, label }
}

